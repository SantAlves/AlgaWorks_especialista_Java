public class Operadores {
    public static void main(String[] args) throws Exception {
        
        // Exemplo 1: Adição

        int a = 5; 
        int b = 10; 
        int soma = a + b; 

        System.out.println("A soma de " + a + " e " + b + " é " + soma); 

        // Exemplo 2: Subtração

        double x = 3.14; 
        double y = 2.71; 
        double diferenca = x - y; 
        
        System.out.println("A diferença entre " + x + " e " + y + " é " + diferenca); 

        // Exemplo 3: Multiplicação

        long c = 1000; 
        long d = 2000; 
        long produto = c * d; 

        System.out.println("O produto de " + c + " e " + d + " é " + produto); 

        // Exemplo 4: Divisão

        float e = 15.0f; 
        float f = 3.0f; 
        float quociente = e / f; 

        System.out.println("O quociente de " + e + " e " + f + " é " + quociente); 

        // Exemplo 5: Resto

        int g = 17; 
        int h = 4; 
        int resto = g % h; 

        System.out.println("O resto de " + g + " dividido por " + h + " é " + resto); 

    }
}
