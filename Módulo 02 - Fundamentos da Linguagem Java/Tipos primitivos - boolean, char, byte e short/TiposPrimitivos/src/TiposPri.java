public class TiposPri {
    public static void main(String[] args) throws Exception {
                
        // Declara e inicializa algumas variáveis de tipos primitivos

        boolean a = true; // Declara uma variável booleana a com o valor true
        byte b = 100; // Declara uma variável byte b com o valor 100
        char c = 'A'; // Declara uma variável char c com o valor 'A'
        short d = 2000; // Declara uma variável short d com o valor 2000
        int e = 3000000; // Declara uma variável int e com o valor 3000000
        long f = 4000000000L; // Declara uma variável long f com o valor 4000000000 (o sufixo L indica que é um literal long)
        float g = 3.14f; // Declara uma variável float g com o valor 3.14 (o sufixo f indica que é um literal float)
        double h = 2.71; // Declara uma variável double h com o valor 2.71

        // Imprime os valores das variáveis
        System.out.println("a = " + a); // a = true
        System.out.println("b = " + b); // b = 100
        System.out.println("c = " + c); // c = A
        System.out.println("d = " + d); // d = 2000
        System.out.println("e = " + e); // e = 3000000
        System.out.println("f = " + f); // f = 4000000000
        System.out.println("g = " + g); // g = 3.14
        System.out.println("h = " + h); // h = 2.71

        
    }
}
